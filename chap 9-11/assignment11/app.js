var num1 = Number(prompt("enter your first number"))
var num2 = Number(prompt("enter your second number"))
var operation = prompt("choose operation +,-,*,/,%")

if (operation=="+")
{
var result = num1+num2
alert(result)

}
if (operation=="-")
{
var result = num1-num2
alert(result)

}
if (operation=="*")
{
var result = num1*num2
alert(result)

}
if (operation=="/")
{
var result = num1/num2
alert(result)

}
if (operation=="%")
{
var result = num1%num2
alert(result)

}
